pm = eval(input("请输入当天 PM2.5 的值:"))

if 0 <= pm < 35:
    s = "空气质量优，请多参加户外活动。"
elif 35 <= pm < 75:
    s = "空气质量良好，可以适当户外活动。"
elif 75 <= pm < 115:
    s = "空气轻度污染，可以少量户外活动。"
elif 115 <= pm < 150:
    s= "空气空气中度污染，请年老体弱人群减少外出。"
elif 150 <= pm < 250:
    s = "空气中度污染，请年老体弱人群不要外出。"
elif pm >= 250:
    s = "空气严重污染，请停止户外作业，若无 PM2.5 口罩，尽量停止外出。"

print("PM2.5 值为{}, {}".format(pm, s))
